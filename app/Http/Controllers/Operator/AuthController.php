<?php

namespace App\Http\Controllers\Operator;

use App\Http\Controllers\Controller;
use Illuminate\Contracts\View\View;
use Illuminate\Http\RedirectResponse;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;
use Illuminate\Support\Facades\Hash;

class AuthController extends Controller
{
    /**
     * --------------------------------------------------------------------------
     * HALAMAN LOGIN
     * --------------------------------------------------------------------------
     */
    public function login(): View
    {
        return view('Operator.Auth.login');
    }

    /**
     * --------------------------------------------------------------------------
     * PROSES LOGIN
     * --------------------------------------------------------------------------
     */
    public function doLogin(Request $request): RedirectResponse
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI INPUT
        |--------------------------------------------------------------------------
        */
        $credentials = $request->validate([
            'username' => ['required', 'string'],
            'password' => ['required', 'string'],
        ]);

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA USER
        |--------------------------------------------------------------------------
        */
        $user = DB::table('users')
            ->where('username', $credentials['username'])
            ->where('is_active', 1)
            ->first();

        /*
        |--------------------------------------------------------------------------
        | USER TIDAK DITEMUKAN
        |--------------------------------------------------------------------------
        */
        if (!$user) {

            return back()
                ->withErrors([
                    'username' => 'Username tidak ditemukan'
                ])
                ->withInput();
        }

        /*
        |--------------------------------------------------------------------------
        | PASSWORD SALAH
        |--------------------------------------------------------------------------
        */
        if (!Hash::check($credentials['password'], $user->password)) {

            return back()
                ->withErrors([
                    'password' => 'Password salah'
                ])
                ->withInput();
        }

        /*
        |--------------------------------------------------------------------------
        | ROLE USER
        |--------------------------------------------------------------------------
        */
        $role = (int) $user->access_type;

        /*
        |--------------------------------------------------------------------------
        | SIMPAN SESSION
        |--------------------------------------------------------------------------
        */
        session([
            'user_id'   => $user->id,
            'username'  => $user->username,
            'role'      => $role,
            'login_at'  => now(),
        ]);

        /*
        |--------------------------------------------------------------------------
        | REDIRECT BERDASARKAN ROLE
        |--------------------------------------------------------------------------
        */
        return match ($role) {

            /*
            |--------------------------------------------------------------------------
            | OPERATOR
            |--------------------------------------------------------------------------
            */
            1 => redirect()
                    ->route('dashboard')
                    ->with('success', 'Selamat datang Operator'),

            /*
            |--------------------------------------------------------------------------
            | KETUA PERTANDINGAN
            |--------------------------------------------------------------------------
            */
            2 => redirect()
                    ->route('ketua.dashboard')
                    ->with('success', 'Selamat datang Ketua Pertandingan'),

            /*
            |--------------------------------------------------------------------------
            | DEWAN
            |--------------------------------------------------------------------------
            */
            3 => redirect()
                    ->route('dewan.dashboard')
                    ->with('success', 'Selamat datang Dewan'),

            /*
            |--------------------------------------------------------------------------
            | TIMER
            |--------------------------------------------------------------------------
            */
            4 => redirect()
                    ->route('timer.dashboard')
                    ->with('success', 'Selamat datang Timer'),

            /*
            |--------------------------------------------------------------------------
            | JURI 1
            |--------------------------------------------------------------------------
            */
            5 => redirect()
                    ->route('juri1')
                    ->with('success', 'Selamat datang Juri 1'),

            /*
            |--------------------------------------------------------------------------
            | JURI 2
            |--------------------------------------------------------------------------
            */
            6 => redirect()
                    ->route('juri2')
                    ->with('success', 'Selamat datang Juri 2'),

            /*
            |--------------------------------------------------------------------------
            | JURI 3
            |--------------------------------------------------------------------------
            */
            7 => redirect()
                    ->route('juri3')
                    ->with('success', 'Selamat datang Juri 3'),

            /*
            |--------------------------------------------------------------------------
            | ROLE TIDAK VALID
            |--------------------------------------------------------------------------
            */
            default => redirect()
                    ->route('login')
                    ->with('error', 'Role tidak dikenali'),
        };
    }

    /**
     * --------------------------------------------------------------------------
     * LOGOUT
     * --------------------------------------------------------------------------
     */
    public function doLogout(): RedirectResponse
    {
        /*
        |--------------------------------------------------------------------------
        | HAPUS SESSION
        |--------------------------------------------------------------------------
        */
        session()->flush();

        /*
        |--------------------------------------------------------------------------
        | REDIRECT LOGIN
        |--------------------------------------------------------------------------
        */
        return redirect()
            ->route('login')
            ->with('success', 'Berhasil logout');
    }

    /**
     * --------------------------------------------------------------------------
     * PROFILE
     * --------------------------------------------------------------------------
     */
    public function profile(): View
    {
        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA USER LOGIN
        |--------------------------------------------------------------------------
        */
        $user = DB::table('users')
            ->where('id', session('user_id'))
            ->first();

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN PROFILE
        |--------------------------------------------------------------------------
        */
        return view('Operator.Auth.profile', compact('user'));
    }
}